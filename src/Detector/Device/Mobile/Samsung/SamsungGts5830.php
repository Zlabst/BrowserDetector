namespace BrowserDetector\Detector\Device\Mobile\Samsung;

use BrowserDetector\Detector\Company;
use UaDeviceType\MobilePhone;
use UaResult\Version;
use UaMatcher\Browser\BrowserInterface;
use UaMatcher\Device\DeviceHasWurflKeyInterface;
use BrowserDetector\Detector\Device\AbstractDevice;
use UaMatcher\Engine\EngineInterface;
use UaMatcher\Os\OsInterface;

/**
 * @category  BrowserDetector
 * @package   BrowserDetector
 * @copyright 2012-2015 Thomas Mueller
 * @license   http://www.opensource.org/licenses/MIT MIT License
 */
class SamsungGts5830 extends AbstractDevice implements DeviceHasWurflKeyInterface
{
    /**
     * the detected browser properties
     *
     * @var array
     */
    protected $properties = array(
        // device
        'code_name'             => 'GT-S5830',
        'model_extra_info'       => null,
        'marketing_name'         => 'Galaxy Ace',
        'has_qwerty_keyboard'    => true,
        'pointing_method'        => 'touchscreen',
        // product info
        'ununiqueness_handler'   => null,
        'uaprof'                 => 'http://wap.samsungmobile.com/uaprof/GT-S5830.xml',
        'uaprof2'                => null,
        'uaprof3'                => null,
        'unique'                 => true,
        // display
        'physical_screen_width'  => 50,
        'physical_screen_height' => 74,
        'columns'                => 25,
        'rows'                   => 21,
        'max_image_width'        => 320,
        'max_image_height'       => 400,
        'resolution_width'       => 320,
        'resolution_height'      => 480,
        'dual_orientation'       => true,
        'colors'                 => 65536,
        // sms
        'sms_enabled'            => true,
        // chips
        'nfc_support'            => false,
    );

    /**
     * checks if this device is able to handle the useragent
     *
     * @return boolean returns TRUE, if this device can handle the useragent
     */
    public function canHandle()
    {
        if (!$this->utils->checkIfContains('GT-S5830')) {
            return false;
        }

        if ($this->utils->checkIfContains(array('GT-S5830i', 'GT-S5830L', 'GT-S5830C'))) {
            return false;
        }

        return true;
    }

    /**
     * gets the weight of the handler, which is used for sorting
     *
     * @return integer
     */
    public function getWeight()
    {
        return 466796;
    }

    /**
     * returns the type of the current device
     *
     * @return \UaDeviceType\TypeInterface
     */
    public function getDeviceType()
    {
        return new MobilePhone();
    }

    /**
     * returns the type of the current device
     *
     * @return \UaMatcher\Company\CompanyInterface
     */
    public function getManufacturer()
    {
        return new Company(new Company\Samsung());
    }

    /**
     * returns the type of the current device
     *
     * @return \BrowserDetector\Detector\Company\AbstractCompany
     */
    public function getBrand()
    {
        return new Company(new Company\Samsung());
    }

    /**
     * returns the WurflKey for the device
     *
     * @param \UaMatcher\Browser\BrowserInterface $browser
     * @param \UaMatcher\Engine\EngineInterface   $engine
     * @param \UaMatcher\Os\OsInterface           $os
     *
     * @return string|null
     */
    public function getWurflKey(BrowserInterface $browser, EngineInterface $engine, OsInterface $os)
    {
        $wurflKey = 'samsung_gt_s5830_ver1';

        $osVersion = $os->detectVersion()->getVersion(
            Version::MAJORMINOR
        );

        switch ((float)$osVersion) {
            case 2.2:
                $wurflKey = 'samsung_gt_s5830_ver1_suban22';
                break;
            case 2.3:
                $wurflKey = 'samsung_gt_s5830_ver1_suban23';
                break;
            case 4.0:
                $wurflKey = 'samsung_gt_s5830_ver1_suban40';
                break;
            default:
                // nothing to do here
                break;
        }

        return $wurflKey;
    }
}
