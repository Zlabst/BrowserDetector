class UnisterPortale extends Unisterbot
{
    /**
     * @var string the detected browser
     */
    protected $_browser = 'UnisterPortale';

    /**
     * @var string the detected manufacturer
     */
    protected $_manufacturer = 'Unister';
    
    /**
     * Returns true if this handler can handle the given user agent
     *
     * @return bool
     */
    public function canHandle()
    {
        if ('' == $this->_useragent) {
            return false;
        }
        
        $portale = array(
            'www.auto.de', 
            'auto.de', 
            'www.kredit.de', 
            'www.geld.de', 
            'www.versicherungen.de', 
            'insurance.preisvergleich.de', 
            'finanzen.shopping.de'
        );
        
        if (!$this->_utils->checkIfContains($portale, true)) {
            return false;
        }
        
        return true;
    }
    
    /**
     * detects the browser version from the given user agent
     *
     * @return string
     */
    protected function _detectVersion()
    {
        $doMatch = preg_match('/UnisterTesting\/([\d\.]+)/', $this->_useragent, $matches);
        
        if ($doMatch) {
            $this->_version = $matches[1];
            return;
        }
        
        $this->_version = '';
    }
    
    /**
     * gets the weight of the handler, which is used for sorting
     *
     * @return integer
     */
    public function getWeight()
    {
        return 300;
    }
}